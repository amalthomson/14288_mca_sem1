"""A program to swap to variables"""

#geting requried input from user for the calculations
var1=int(input("enter the value of var1 :"))
var2=int(input("enter the value of var2 :"))

#swaping the values
var1, var2 = var2, var1

#printing the output
print("New value of var1 is :", var1)
print("New value of var2 is :", var2)

